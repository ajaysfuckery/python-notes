import math

pi = 3.14
x = 1
y = 2
z = 3

print(pi)
print(round(pi))
print(math.ceil(pi))        # rounds up
print(math.floor(pi))       # rounds down
print(abs(pi))      # Tells how far number is from 0
print(pow(pi, 2))   # pi to power of 2
print(math.sqrt(420))   # squareroot

print(max(x, y, z))